package Switch;

public class SwitchString {
public static void main(String[] args) {
	String monthvalue="A";
	
	switch(monthvalue) {
	
	case "J":
		System.out.println("january");
		break;
	case "F":
		System.out.println("FEB");
		break;
	case "M":
		System.out.println("march");
		break;
	case "A":
		System.out.println("april");
		break;
	case "ma":
		System.out.println("ma");
		break;
		default:
			System.out.println();
		
	}
}


}

