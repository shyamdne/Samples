package Array;

public class ChangeArray {
	
	public static void main(String[] args) {
		
		int [] newarray = {1,2,3,4,5,6};
		
		System.out.println("befor changing Array:     "+ newarray[0]);
		
		
		System.out.println();
		
		
		newarray [0] = 10;
		System.out.println("after changing Array:    "+ newarray[0]);
	}
	

}
